#data type declarations in python
#python uses implicit declaration and not explicit
#
#int x = 10 // str x = "ten"    is explicit declaration
# x = 10 // x = "10"    is implicit declaration

x = "10"        #String
y = 10          #integer
z = 10.1        #float

sum1 = x + x
sum2 = y + y

print(sum1)
print(sum2)

print(type(x), type(y), type(z))        #type function shows the type of data that is stored in the variable